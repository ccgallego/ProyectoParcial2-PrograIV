    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String Cedula = txttCedula.getText();
        Pasajero pasajero = new Pasajero();
        boolean Encontrado = false;
        for (int i = 0; i <= 49; i++) {
            pasajero = avion.silla[i].getPasajero();
            String BuscaCedula = avion.silla[i].getPasajero().getCedula();
            if (BuscaCedula == null ? Cedula == null : BuscaCedula.equals(Cedula)) {
                String Nombre = avion.silla[i].getPasajero().getNombre();
                System.out.println(Nombre);
                String Clase = avion.silla[i].getClase();
                String Posicion = avion.silla[i].getPosicion();
                String Numero = String.valueOf(avion.silla[i].getNumero());
                jLabelNombre.setText(Nombre);
                jLabelClase.setText(Clase);
                jLabelPosicion.setText(Posicion);
                jLabelSilla.setText(Numero);
                Encontrado = true;

            }

        }
        if (!Encontrado) {
            JOptionPane.showMessageDialog(null, " No realizaste ninguna reserva ");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

