    public String[] getPosicion(String Clase) {

        String[] Posicion = new String[4];
        if ("Ejecutiva".equals(Clase)) {
            Posicion[0] = "Seleccione..";
            Posicion[1] = "Ventana";
            Posicion[2] = "Pasillo";
        } else if ("Economica".equals(Clase)) {
            Posicion[0] = "Seleccione..";
            Posicion[1] = "Ventana";
            Posicion[2] = "Centro";
            Posicion[3] = "Pasillo";
        }
        return Posicion;
    }

    public String[] getSillas(String Clase, String Posicion) {
        int tamDisponibilidad = 0;
        String[] sillon = new String[49];
        for (int i = 0; i <= 49; i++) {
            if (avion.silla[i].disponibilidad == true && avion.silla[i].clase.equals(Clase) && avion.silla[i].posicion.equals(Posicion)) {
                sillon[tamDisponibilidad] = String.valueOf(avion.silla[i].numero);
                tamDisponibilidad += 1;
            }
        }
        return sillon;

    }

