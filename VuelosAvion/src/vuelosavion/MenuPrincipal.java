    public int Ocupacion() {
        int CuentaSillascupadas = 0;
        for (int i = 0; i <= 49; i++) {
            if (avion.silla[i].isDisponibilidad() == false) {
                puesto[i].setBackground(Color.RED);
                CuentaSillascupadas++;
            } else {
                puesto[i].setBackground(Color.CYAN);
            }
        }
        return CuentaSillascupadas;
    }

